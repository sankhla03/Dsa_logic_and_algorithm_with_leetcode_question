package Bit_manipulation;

public class operations {
    public static void main(String[] args) {
        int a = 5; // 0101 in binary
        int b = 2; // 0011 in binary

        // AND operation
       System.out.println(a &b);
        System.out.println(a |b);
        System.out.println(a ^b);
        System.out.println(~a);
        System.out.println(a << b);
        System.out.println(a >> b);
         System.out.println(5^5);
    }
}
